import { ComponentFixture, TestBed } from '@angular/core/testing';
import { of } from 'rxjs';
import { HttpHeaders, HttpResponse } from '@angular/common/http';
import { ActivatedRoute, convertToParamMap } from '@angular/router';

import { JhipsterSeamstressApplicationTestModule } from '../../../test.module';
import { CustOrderComponent } from 'app/entities/cust-order/cust-order.component';
import { CustOrderService } from 'app/entities/cust-order/cust-order.service';
import { CustOrder } from 'app/shared/model/cust-order.model';

describe('Component Tests', () => {
  describe('CustOrder Management Component', () => {
    let comp: CustOrderComponent;
    let fixture: ComponentFixture<CustOrderComponent>;
    let service: CustOrderService;

    beforeEach(() => {
      TestBed.configureTestingModule({
        imports: [JhipsterSeamstressApplicationTestModule],
        declarations: [CustOrderComponent],
        providers: [
          {
            provide: ActivatedRoute,
            useValue: {
              data: of({
                defaultSort: 'id,asc',
              }),
              queryParamMap: of(
                convertToParamMap({
                  page: '1',
                  size: '1',
                  sort: 'id,desc',
                })
              ),
            },
          },
        ],
      })
        .overrideTemplate(CustOrderComponent, '')
        .compileComponents();

      fixture = TestBed.createComponent(CustOrderComponent);
      comp = fixture.componentInstance;
      service = fixture.debugElement.injector.get(CustOrderService);
    });

    it('Should call load all on init', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new CustOrder(123)],
            headers,
          })
        )
      );

      // WHEN
      comp.ngOnInit();

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.custOrders && comp.custOrders[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });

    it('should load a page', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new CustOrder(123)],
            headers,
          })
        )
      );

      // WHEN
      comp.loadPage(1);

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.custOrders && comp.custOrders[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });

    it('should calculate the sort attribute for an id', () => {
      // WHEN
      comp.ngOnInit();
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['id,desc']);
    });

    it('should calculate the sort attribute for a non-id attribute', () => {
      // INIT
      comp.ngOnInit();

      // GIVEN
      comp.predicate = 'name';

      // WHEN
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['name,desc', 'id']);
    });
  });
});
